package com.shshin.har2jmx.exception;

public class HarParsorException extends Exception {
    public HarParsorException() {
        super();
    }

    public HarParsorException(String message) {
        super(message);
    }

    public HarParsorException(String message, Throwable cause) {
        super(message, cause);
    }

    public HarParsorException(Throwable cause) {
        super(cause);
    }
}
